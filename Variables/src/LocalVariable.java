/*the variables which are declared inside the class and inside the body is called as Local Variable
   these variables can  be accesed within the body only.We cannot access these variables outside the body
 */
public class LocalVariable 
{
	
	public static void main(String args[])
	{
		int a=10,b=20;
		
		System.out.println("a ="+a);
		System.out.println("b ="+b);
	}
	
}